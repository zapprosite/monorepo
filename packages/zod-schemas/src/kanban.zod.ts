import z from 'zod';
import { kanbanCardPriorityZod, kanbanCardStatusZod } from './crm_enums.zod.js';
import { zText, zTimestamps } from './zod_utils.js';

// ---------------------------------------------------------------------------
// KanbanBoard
// ---------------------------------------------------------------------------

const boardMandatoryZod = z.object({
	nome: zText(1),
	setor: zText(1),
});

const boardOptionalZod = z.object({
	descricao: zText().nullable(),
	cor: z.string().max(7).nullable(),
});

const boardAutoGeneratedZod = z.object({
	boardId: z.string().uuid(),
});

export const boardCreateInputZod = boardMandatoryZod.extend(boardOptionalZod.partial().shape);
export const boardUpdateInputZod = boardMandatoryZod
	.extend(boardOptionalZod.shape)
	.extend(boardAutoGeneratedZod.shape)
	.partial()
	.required({ boardId: true });
export const boardSelectAllZod = boardMandatoryZod
	.extend(boardOptionalZod.shape)
	.extend(boardAutoGeneratedZod.shape)
	.extend(zTimestamps);
export const boardGetByIdZod = z.object({ boardId: z.string().uuid() });
export const listBoardFilterZod = z.object({
	setor: zText().optional(),
});

export type BoardCreateInput = z.infer<typeof boardCreateInputZod>;
export type BoardUpdateInput = z.infer<typeof boardUpdateInputZod>;
export type BoardSelectAll = z.infer<typeof boardSelectAllZod>;

