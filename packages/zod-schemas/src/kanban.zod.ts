// ---------------------------------------------------------------------------
// KanbanCard
// ---------------------------------------------------------------------------

const cardMandatoryZod = z.object({
	columnId: z.string().uuid(),
	titulo: zText(1),
});

const cardOptionalZod = z.object({
	descricao: zText().nullable(),
	prioridade: kanbanCardPriorityZod.nullable(),
	status: kanbanCardStatusZod.nullable(),
	responsavelId: z.string().uuid().nullable(),
	dataVencimento: z.string().date().nullable(),
	ordem: z.number().int().min(0).nullable(),
});

const cardAutoGeneratedZod = z.object({
	cardId: z.string().uuid(),
});

export const cardCreateInputZod = cardMandatoryZod.extend(cardOptionalZod.partial().shape);
export const cardUpdateInputZod = cardMandatoryZod
	.extend(cardOptionalZod.shape)
	.extend(cardAutoGeneratedZod.shape)
	.partial()
	.required({ cardId: true });
export const cardSelectAllZod = cardMandatoryZod
	.extend(cardOptionalZod.shape)
	.extend(cardAutoGeneratedZod.shape)
	.extend(zTimestamps);
export const cardGetByIdZod = z.object({ cardId: z.string().uuid() });
export const listCardFilterZod = z.object({
	columnId: z.string().uuid().optional(),
	boardId: z.string().uuid().optional(),
	status: kanbanCardStatusZod.optional(),
	responsavelId: z.string().uuid().optional(),
});

export type CardCreateInput = z.infer<typeof cardCreateInputZod>;
export type CardUpdateInput = z.infer<typeof cardUpdateInputZod>;
export type CardSelectAll = z.infer<typeof cardSelectAllZod>;
